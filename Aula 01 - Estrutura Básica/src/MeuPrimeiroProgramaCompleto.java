// Exemplo completo do Primeiro Programa Java
// Declaração da classe, que é o "envelope" do seu programa.
// nome da classe é "MeuPrimeiroPrograma".
public class MeuPrimeiroProgramaCompleto {
    // método main e o ponto de partida do seu programa.
    // Tudo que estiver aqui dentro será executado quando o programa iniciar.
    public static void main(String[] args) {
        // Esta é a instrução para imprimir uma mensagem na tela.
        // Lembre-se do ponto e virgula no final de cada instrução!
        System.out.println("Olá, mundo! Este é o meu primeiro programa Java!");
    } // Fim do método main
} // Fim da classe MeuPrimeiroPrograma
